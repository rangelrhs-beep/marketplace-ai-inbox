def safe_log_payload(payload: Any, *, max_length: int = 2000) -> str:
    try:
        text = json.dumps(payload, ensure_ascii=False, default=str)
    except Exception:
        text = repr(payload)
    if len(text) > max_length:
        return f"{text[:max_length]}... [truncated]"
    return text


