def list_integrations_health(db: Session = Depends(get_db)):
    health_items = []
    for service in integration_services.values():
        try:
            if service.client.id == "mercado-livre":
                integration = get_ml_integration(db)
                connected = False
                token_status = integration.token_status or "missing"
                last_error = "Mercado Livre não conectado."
                if integration.access_token or integration.refresh_token:
                    try:
                        get_valid_mercadolivre_token(db)
                        db.refresh(integration)
                        connected = True
                        token_status = "valid"
                        last_error = None
                    except HTTPException as error:
                        db.refresh(integration)
                        token_status = integration.token_status or "expired"
                        last_error = "Mercado Livre token expirado. Reconecte a integração."
                        logger.warning("Mercado Livre health token refresh failed: %s", error.detail)
                health_items.append(
                    IntegrationHealth(
                        id=service.client.id,
                        channel=service.client.channel,
                        connected=connected,
                        api_status="operational" if connected else "down",
                        last_sync=format_optional_datetime(integration.last_sync),
                        last_error=last_error,
                        token_status=token_status,
                    )
                )
                continue
            health = service.get_health()
            health_items.append(
                IntegrationHealth(
                    id=health.id,
                    channel=health.channel,
                    connected=health.token_status in {"valid", "not_required"},
                    api_status=health.api_status,
                    last_sync=format_optional_datetime(health.last_sync)
                    if isinstance(health.last_sync, datetime)
                    else health.last_sync,
                    last_error=health.last_error,
                    token_status=health.token_status,
                )
            )
        except ConnectorError as error:
            health_items.append(
                IntegrationHealth(
                    id=service.client.id,
                    channel=service.client.channel,
                    connected=False,
                    api_status="down",
                    last_sync=None,
                    last_error=error.message,
                    token_status="missing",
                )
            )
    return health_items


