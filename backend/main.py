def get_valid_mercadolivre_token(db: Session | None = None, *, force_refresh: bool = False) -> str:
    owns_session = db is None
    session = db or SessionLocal()
    try:
        integration = get_ml_integration(session)
        if not integration.access_token and not integration.refresh_token:
            logger.warning("Mercado Livre token unavailable: no access_token or refresh_token")
            raise HTTPException(status_code=401, detail="Mercado Livre não conectado.")

        refresh_threshold = datetime.utcnow() + timedelta(minutes=5)
        if (
            integration.access_token
            and integration.expires_at
            and integration.expires_at > refresh_threshold
            and not force_refresh
        ):
            logger.info("Mercado Livre token still valid until %s", integration.expires_at.isoformat())
            return integration.access_token

        if not integration.refresh_token:
            logger.warning("Mercado Livre token refresh failed: missing refresh_token")
            integration.token_status = "expired"
            integration.updated_at = datetime.utcnow()
            session.commit()
            raise HTTPException(status_code=401, detail="Mercado Livre refresh_token is missing")

        logger.info("Mercado Livre token refresh attempted")
        config = get_ml_config()
        try:
            refreshed = ml_request_json(
                f"{ML_API_BASE_URL}/oauth/token",
                method="POST",
                data={
                    "grant_type": "refresh_token",
                    "client_id": config["client_id"],
                    "client_secret": config["client_secret"],
                    "refresh_token": integration.refresh_token,
                },
            )
        except HTTPException:
            logger.exception("Mercado Livre token refresh failed")
            integration.token_status = "expired"
            integration.updated_at = datetime.utcnow()
            session.commit()
            raise

        expires_in = int(refreshed.get("expires_in") or integration.expires_in or 0)
        integration.access_token = refreshed.get("access_token") or integration.access_token
        integration.refresh_token = refreshed.get("refresh_token") or integration.refresh_token
        integration.expires_in = expires_in
        integration.expires_at = datetime.utcnow() + timedelta(seconds=expires_in)
        integration.seller_id = str(refreshed.get("user_id") or refreshed.get("seller_id") or integration.seller_id or "")
        integration.token_status = "valid"
        integration.updated_at = datetime.utcnow()
        session.commit()
        logger.info("Mercado Livre token refresh succeeded; expires_at=%s", integration.expires_at.isoformat())
        if not integration.access_token:
            raise HTTPException(status_code=401, detail="Mercado Livre token refresh did not return access_token")
        return integration.access_token
    finally:
        if owns_session:
            session.close()


def get_ml_seller_id(tokens: dict[str, Any]) -> str:
    seller_id = tokens.get("seller_id") or tokens.get("user_id")
    if seller_id:
        return str(seller_id)

    me = ml_request_json(f"{ML_API_BASE_URL}/users/me", access_token=tokens["access_token"])
    seller_id = me.get("id")
    if not seller_id:
        raise HTTPException(status_code=502, detail="Could not resolve Mercado Livre seller_id")
    update_ml_tokens({"seller_id": seller_id})
    return str(seller_id)


def normalize_ml_question_status(raw_status: str | None) -> str:
    status = (raw_status or "").lower()
    if status in {"unanswered", "pending", "opened", "active"}:
        return "Pendente"
    if status in {"answered", "closed"}:
        return "Respondida"
    return "Pendente"


def get_ml_product_title(item_id: str | None, access_token: str) -> str:
    if not item_id:
        return "Produto Mercado Livre"
    try:
        item = ml_request_json(f"{ML_API_BASE_URL}/items/{item_id}", access_token=access_token)
        return item.get("title") or item_id
    except HTTPException:
        return item_id


def normalize_ml_question(payload: dict[str, Any], access_token: str) -> dict[str, Any]:
    item_id = payload.get("item_id") or payload.get("item", {}).get("id")
    text = payload.get("text") or payload.get("question") or payload.get("body") or ""
    created_at = payload.get("date_created") or payload.get("created_at") or datetime.utcnow().isoformat()
    return {
        "channel": "mercado_livre",
        "external_id": str(payload.get("id") or payload.get("question_id") or ""),
        "product_title": get_ml_product_title(item_id, access_token),
        "question_text": text,
        "status": normalize_ml_question_status(payload.get("status")),
        "created_at": created_at,
        "raw_payload": payload,
    }


def upsert_ml_question(db: Session, normalized: dict[str, Any]) -> QuestionRecord:
    external_id = str(normalized.get("external_id") or "")
    if not external_id:
        raise HTTPException(status_code=502, detail="Mercado Livre question missing external_id")

    question = (
        db.query(QuestionRecord)
        .filter(
            QuestionRecord.company_id == DEFAULT_COMPANY_ID,
            QuestionRecord.provider == DEFAULT_PROVIDER,
            QuestionRecord.external_id == external_id,
        )
        .first()
    )
    if not question:
        question = QuestionRecord(
            company_id=DEFAULT_COMPANY_ID,
            provider=DEFAULT_PROVIDER,
            external_id=external_id,
        )
        db.add(question)

    question.product_title = normalized.get("product_title") or "Produto Mercado Livre"
    question.question_text = normalized.get("question_text") or ""
    question.status = normalize_db_status(normalized.get("status"))
    question.created_at = parse_datetime(normalized.get("created_at"))
    question.raw_payload = normalized.get("raw_payload") or {}
    question.updated_at = datetime.utcnow()
    return question


def ensure_initial_suggestion(db: Session, question: QuestionRecord) -> None:
    existing = question.ai_suggestion
    if existing and (existing.original_suggestion or existing.final_response):
        return
    settings = get_default_settings(db)
    related_products = search_related_products(
        f"{question.product_title or ''} {question.question_text or ''}",
        limit=5,
        db=db,
    )
    logger.info(
        "Injecting related products into initial AI suggestion external_id=%s count=%s",
        question.external_id,
        len(related_products),
    )
    try:
        suggestion_text = generate_openai_initial_suggestion(
            product_title=question.product_title,
            question_text=question.question_text,
            settings=settings,
            related_products=related_products,
        )
        logger.info("AI initial suggestion generated external_id=%s", question.external_id)
    except Exception as error:
        logger.exception("Failed to generate initial AI suggestion for question %s", question.external_id)
        suggestion_text = (
            "Não foi possível gerar a sugestão inicial da IA. "
            "Revise a pergunta e escreva uma resposta antes de enviar."
        )
    if existing:
        existing.original_suggestion = suggestion_text
        existing.suggestion_text = suggestion_text
        existing.final_response = suggestion_text
        existing.final_answer = suggestion_text
        existing.updated_at = datetime.utcnow()
    else:
        suggestion = AiSuggestion(
            question=question,
            original_suggestion=suggestion_text,
            suggestion_text=suggestion_text,
            final_response=suggestion_text,
            final_answer=suggestion_text,
        )
        db.add(suggestion)
        question.ai_suggestion = suggestion
    db.flush()


