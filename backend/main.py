def extract_search_terms(text_value: str) -> list[str]:
    normalized = (
        text_value.lower()
        .replace("/", " ")
        .replace("-", " ")
        .replace("_", " ")
    )
    stopwords = {
        "para", "com", "sem", "uma", "uns", "das", "dos", "que", "tem", "esse", "essa",
        "este", "esta", "produto", "pergunta", "cliente", "serve", "posso", "pode",
        "qual", "como", "quando", "onde", "mais", "menos", "mercado", "livre",
    }
    terms: list[str] = []
    for raw_term in normalized.split():
        term = raw_term.strip(".,;:!?()[]{}\"'")
        if len(term) < 3 or term in stopwords or term.isdigit():
            continue
        if term not in terms:
            terms.append(term)
    return terms[:12]


def score_product_relevance(product: ProductCache, terms: list[str], question_text: str) -> int:
    searchable = " ".join(
        [
            product.title or "",
            product.seller_custom_field or "",
            json.dumps(product.attributes_json or [], ensure_ascii=False),
        ]
    ).lower()
    score = 0
    for term in terms:
        if term in (product.title or "").lower():
            score += 4
        if term in (product.seller_custom_field or "").lower():
            score += 3
        if term in searchable:
            score += 1
    title = (product.title or "").lower()
    if title and title in question_text.lower():
        score += 8
    return score


def search_related_products(question_text: str, limit: int = 5, db: Session | None = None) -> list[dict[str, Any]]:
    owns_session = db is None
    session = db or SessionLocal()
    try:
        try:
            terms = extract_search_terms(question_text)
            if not terms:
                logger.info("Related product search skipped: no useful terms")
                return []
            filters = []
            for term in terms:
                pattern = f"%{term}%"
                filters.append(ProductCache.title.ilike(pattern))
                filters.append(ProductCache.seller_custom_field.ilike(pattern))
                filters.append(cast(ProductCache.attributes_json, Text).ilike(pattern))
            candidates = (
                session.query(ProductCache)
                .filter(
                    ProductCache.company_id == DEFAULT_COMPANY_ID,
                    ProductCache.provider == DEFAULT_PROVIDER,
                    ProductCache.status == "active",
                    or_(*filters),
                )
                .order_by(ProductCache.updated_at.desc())
                .limit(50)
                .all()
            )
            scored = [
                (score_product_relevance(product, terms, question_text), product)
                for product in candidates
            ]
            scored = [(score, product) for score, product in scored if score > 0]
            scored.sort(key=lambda item: item[0], reverse=True)
            products = [product_to_api(product) for _, product in scored[:limit]]
            logger.info("Related products found count=%s terms=%s", len(products), terms[:6])
            return products
        except Exception:
            logger.exception("Related product search failed; continuing without product context")
            return []
    finally:
        if owns_session:
            session.close()


def upsert_ml_product(db: Session, item_payload: dict[str, Any]) -> tuple[ProductCache, bool]:
    external_id = str(item_payload.get("id") or "")
    if not external_id:
        raise ValueError("Mercado Livre product missing id")
    product = (
        db.query(ProductCache)
        .filter(
            ProductCache.company_id == DEFAULT_COMPANY_ID,
            ProductCache.provider == DEFAULT_PROVIDER,
            ProductCache.external_id == external_id,
        )
        .first()
    )
    created = product is None
    if created:
        product = ProductCache(
            company_id=DEFAULT_COMPANY_ID,
            provider=DEFAULT_PROVIDER,
            external_id=external_id,
        )
        db.add(product)
    product.title = item_payload.get("title") or ""
    product.permalink = item_payload.get("permalink") or ""
    product.price = str(item_payload.get("price")) if item_payload.get("price") is not None else None
    product.currency_id = item_payload.get("currency_id")
    product.status = item_payload.get("status")
    product.available_quantity = item_payload.get("available_quantity")
    product.thumbnail = item_payload.get("thumbnail")
    product.category_id = item_payload.get("category_id")
    product.listing_type_id = item_payload.get("listing_type_id")
    product.condition = item_payload.get("condition")
    product.seller_custom_field = item_payload.get("seller_custom_field")
    product.attributes_json = item_payload.get("attributes") or []
    product.variations_json = item_payload.get("variations") or []
    product.raw_payload = item_payload
    product.last_synced_at = datetime.utcnow()
    product.updated_at = datetime.utcnow()
    return product, created


def sync_mercadolivre_products(db: Session) -> dict[str, Any]:
    logger.info("Mercado Livre products sync started")
    access_token = get_valid_mercadolivre_token(db)
    integration = get_ml_integration(db)
    seller_id = integration.seller_id or get_ml_seller_id(
        {"access_token": access_token, "seller_id": integration.seller_id}
    )
    try:
        item_ids = fetch_ml_item_ids_by_seller(str(seller_id), access_token)
    except HTTPException as error:
        if error.status_code != 401:
            raise
        access_token = get_valid_mercadolivre_token(db, force_refresh=True)
        item_ids = fetch_ml_item_ids_by_seller(str(seller_id), access_token)
    logger.info("Mercado Livre product item ids fetched count=%s", len(item_ids))

    stats = {
        "fetched": len(item_ids),
        "created": 0,
        "updated": 0,
        "active": 0,
        "inactive": 0,
    }

    for item_id in item_ids:
        try:
            try:
                item_payload = ml_request_json(f"{ML_API_BASE_URL}/items/{quote(str(item_id), safe='')}", access_token=access_token)
            except HTTPException as error:
                if error.status_code != 401:
                    raise
                access_token = get_valid_mercadolivre_token(db, force_refresh=True)
                item_payload = ml_request_json(f"{ML_API_BASE_URL}/items/{quote(str(item_id), safe='')}", access_token=access_token)
            product, created = upsert_ml_product(db, item_payload)
            stats["created" if created else "updated"] += 1
            if product.status == "active":
                stats["active"] += 1
            else:
                stats["inactive"] += 1
        except Exception as error:
            logger.exception("Mercado Livre product sync item failed item_id=%s error=%s", item_id, error)
            continue

    db.commit()
    logger.info(
        "Mercado Livre products sync finished fetched=%s created=%s updated=%s active=%s inactive=%s",
        stats["fetched"],
        stats["created"],
        stats["updated"],
        stats["active"],
        stats["inactive"],
    )
    return stats


def sync_mercadolivre_questions(db: Session, *, source: str = "manual") -> dict[str, Any]:
    logger.info("Mercado Livre sync started source=%s", source)
    stats = {
        "created": 0,
        "updated": 0,
        "questions_fetched": 0,
        "suggestions_generated": 0,
        "marked_responded": 0,
    }

    def run_with_tokens(force_refresh: bool = False) -> dict[str, Any]:
        access_token = get_valid_mercadolivre_token(db, force_refresh=force_refresh)
        integration = get_ml_integration(db)
        tokens = {
            "access_token": access_token,
            "seller_id": integration.seller_id,
            "user_id": integration.seller_id,
        }
        seller_id = get_ml_seller_id(tokens)
        raw_response = fetch_ml_questions_by_seller(
            seller_id=seller_id,
            access_token=tokens["access_token"],
        )
        logger.info("Mercado Livre questions raw response source=%s body=%s", source, raw_response)
        print(f"Mercado Livre questions raw response source={source}: {raw_response}")

        fetched_external_ids: set[str] = set()
        saved_questions: list[QuestionRecord] = []
        questions_payload = extract_ml_questions(raw_response)
        stats["questions_fetched"] = len(questions_payload)

        for question_payload in questions_payload:
            normalized = normalize_ml_question(question_payload, tokens["access_token"])
            external_id = str(normalized.get("external_id") or "")
            if not external_id:
                logger.warning("Mercado Livre sync ignored question without external_id source=%s payload=%s", source, question_payload)
                continue
            fetched_external_ids.add(external_id)
            existing_question = (
                db.query(QuestionRecord)
                .filter(
                    QuestionRecord.company_id == DEFAULT_COMPANY_ID,
                    QuestionRecord.provider == DEFAULT_PROVIDER,
                    QuestionRecord.external_id == external_id,
                )
                .first()
            )
            had_suggestion = bool(get_suggestion_text(existing_question.ai_suggestion)) if existing_question else False
            question = upsert_ml_question(db, normalized)
            stats["updated" if existing_question else "created"] += 1
            ensure_initial_suggestion(db, question)
            if not had_suggestion and get_suggestion_text(question.ai_suggestion):
                stats["suggestions_generated"] += 1
            saved_questions.append(question)

        local_pending_questions = (
            db.query(QuestionRecord)
            .filter(
                QuestionRecord.company_id == DEFAULT_COMPANY_ID,
                QuestionRecord.provider == DEFAULT_PROVIDER,
                QuestionRecord.status == "pending",
            )
            .all()
        )
        for question in local_pending_questions:
            if question.external_id in fetched_external_ids:
                continue
            try:
                question_detail = fetch_ml_question_detail(question.external_id, tokens["access_token"])
            except HTTPException as error:
                logger.warning(
                    "Could not verify pending ML question during sync source=%s external_id=%s detail=%s",
                    source,
                    question.external_id,
                    error.detail,
                )
                continue
            if not ml_question_is_answered(question_detail):
                continue
            final_answer = extract_ml_answer_text(question_detail) or "Respondida no Mercado Livre."
            updated_question = save_answered_question_state(
                db,
                question_id=question.external_id,
                approved_answer=final_answer,
                payload=MercadoLivreAnswerPayload(answer=final_answer),
                raw_response=question_detail,
            )
            stats["marked_responded"] += 1
            if updated_question:
                saved_questions.append(updated_question)

        integration = get_ml_integration(db)
        integration.last_sync = datetime.utcnow()
        integration.token_status = "valid"
        db.commit()
        logger.info(
            "Mercado Livre sync finished source=%s fetched=%s created=%s updated=%s suggestions=%s marked_responded=%s",
            source,
            stats["questions_fetched"],
            stats["created"],
            stats["updated"],
            stats["suggestions_generated"],
            stats["marked_responded"],
        )
        return {
            "questions": [question_to_api(question, db=db) for question in saved_questions],
            "stats": stats,
        }

    try:
        return run_with_tokens()
    except HTTPException as error:
        if error.status_code != 401:
            logger.exception("Mercado Livre sync failed source=%s", source)
            raise
        logger.info("Mercado Livre sync refreshing token source=%s", source)
        return run_with_tokens(force_refresh=True)


