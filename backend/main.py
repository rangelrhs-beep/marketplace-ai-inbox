def fetch_ml_item_ids_by_seller(seller_id: str, access_token: str) -> list[str]:
    item_ids: list[str] = []
    offset = 0
    limit = 50
    while True:
        query = urlencode({"limit": limit, "offset": offset})
        response = ml_request_json(
            f"{ML_API_BASE_URL}/users/{seller_id}/items/search?{query}",
            access_token=access_token,
        )
        results = response.get("results") if isinstance(response, dict) else []
        if not isinstance(results, list) or not results:
            break
        item_ids.extend(str(item_id) for item_id in results if item_id)
        paging = response.get("paging") or {}
        total = int(paging.get("total") or len(item_ids))
        offset += len(results)
        if offset >= total:
            break
    return item_ids


